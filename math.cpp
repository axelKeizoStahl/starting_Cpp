#include <iostream>
#include <cmath> //this is needed for more complex math functions like log, round, and sqrt
using namespace std;

int main() {

    //max(x, y) tells which number is higher
    cout << max(12, 13);

    cout << "\n";


    cout << sqrt(16); //square root

    cout << "\n";

    cout << round(2.3); //round a number

    cout << "\n";

    cout << log(2); //natural loagarithm

    cout << "\n";

    return 0;
}